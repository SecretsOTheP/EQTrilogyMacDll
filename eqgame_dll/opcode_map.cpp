std::map<unsigned long, std::string> eqmac_opcode_map;

std::string get_opcode_name(unsigned long opcode)
{
std::map<unsigned long, std::string>::iterator itr;

	return (itr= eqmac_opcode_map.find(opcode))!=eqmac_opcode_map.end() ? itr->second : "OP_Unknown";
}

uint16_t get_opcode_id(std::string name)
{
	std::map<unsigned long, std::string>::iterator itr;
	for (itr = eqmac_opcode_map.begin(); itr != eqmac_opcode_map.end(); ++itr) {
		if (itr->second == name) return itr->first;
	}
	return 0x0000;
}

